/// Enum defining different types of animations for SuperText widget.
enum SuperTextType {
  /// Text will scale in and out.
  scale,

  /// Text will blink on and off.
  blink,

  /// Text will slide up.
  slideUp,

  /// Text will slide down.
  slideDown,

  /// Text will slide left.
  slideLeft,

  /// Text will slide right.
  slideRight,
}
